#include <stdio.h>

int main()
{
    
    int szam;
    
    szam = 2021;
    printf("Évszám: %d\n", szam);
    printf("--------------------------\n");
    
    char betu;
    betu = 'L';
    printf("Kedvenc betűm: %c\n", betu);
    printf("--------------------------\n");
    
    float pi;
    
    pi = 3.14159;
    printf("pi értéke: %f\n", pi);
    printf("--------------------------\n");
    
    double hossz;
    hossz = 1.2345678;
    printf("hossz: %lf\n", hossz);
    printf("--------------------------\n");   
    
    return 0;
}
    
